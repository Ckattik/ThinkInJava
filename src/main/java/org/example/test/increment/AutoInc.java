package org.example.test.increment;

public class AutoInc {
    public static void main(String [] args){

        int i = 1;
        System.out.println(" i :" + i);

        System.out.println("++i :" + ++i);   //Префиксный инкремент
        System.out.println("i++ :" + i++);   //Постфиксный инкремент . После вывода в консоль увеличиться на +1

        System.out.println(" i :" + i);

        System.out.println(" --i :" + --i);
        System.out.println(" i-- :" + i--);

        System.out.println(" i :" + i);





    }
}
